      <p><span class="bold">У</span>точненные данные по темпам роста ВВП во II квартале 2015 года вышли и в Великобритании. Показатель полностью совпал с первоначальной оценкой роста на 0,7% к/к. Более интересной для инвесторов стал выход макростатистики с рынка недвижимости Великобритании. Согласно отчету Ассоциации британских банкиров количество одобренных ипотечных кредитов в Великобритании в июле этого года выросло до 17-месячного максимума в 46 тыс. одобренных заявок, увеличившись в годовом выражении на 7,7%.</p>
      <p class="mt15">При этом валовый объем ипотечных заимствований в июле вырос на 11,5% г/г до 11,8 млрд. фунтов стерлингов. По мнению представителей Ассоциации наиболее осведомленные граждане стараются приобрести недвижимость перед ожидаемым увеличением процентных ставок, как в самой Великобритании, так и в США. Однако в секторе наметилось небольшое замедление темпов роста. В августе цены на жилье в стране выросли на 3,2% г/г - это минимальный показатель с июня 2013 года.</p>
      
      <p class="mt15">Представители Национальной ассоциации строителей Великобритании связывают данный "спад" с высокой базой августа прошлого года.</p>
