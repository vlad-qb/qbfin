      <p class="mt15">Одной из главных причин подобной динамики остается снижение цен на сырьевые товары и сокращение внешнеторгового оборота в мире на 0,5% к/к во II квартале этого года. Дополнительное влияние на уменьшение внешнеторгового оборота продолжает оказывать несбалансированность курсов валют ключевых экспортных стран. Сохранение данного дисбаланса может послужить одной из причин замедления темпов роста мировой экономики в 2015-2016 гг.</p>
